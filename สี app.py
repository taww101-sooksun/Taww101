import streamlit as st
from streamlit_js_eval import get_geolocation
from datetime import datetime
import pytz
import pandas as pd

st.title("üìç ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡πÅ‡∏•‡∏∞‡πÄ‡∏ß‡∏•‡∏≤‡∏à‡∏£‡∏¥‡∏á")

# 1. ‡∏î‡∏∂‡∏á‡∏û‡∏¥‡∏Å‡∏±‡∏î (‡∏´‡πà‡∏≠‡∏´‡∏∏‡πâ‡∏°‡∏î‡πâ‡∏ß‡∏¢ Container ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏∞‡∏≠‡∏≤‡∏î)
# ‡πÉ‡∏™‡πà‡∏™‡πà‡∏ß‡∏ô‡∏ô‡∏µ‡πâ‡πÑ‡∏ß‡πâ‡∏ï‡πâ‡∏ô‡πÜ ‡πÅ‡∏•‡∏∞‡πÄ‡∏ä‡πá‡∏Å‡πÄ‡∏á‡∏∑‡πà‡∏≠‡∏ô‡πÑ‡∏Ç‡πÉ‡∏´‡πâ‡∏î‡∏µ
location = get_geolocation()

# 2. ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡πÄ‡∏ß‡∏•‡∏≤‡∏õ‡∏£‡∏∞‡πÄ‡∏ó‡∏®‡πÑ‡∏ó‡∏¢
tz_th = pytz.timezone('Asia/Bangkok')
now_th = datetime.now(tz_th)

# 3. ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÄ‡∏á‡∏∑‡πà‡∏≠‡∏ô‡πÑ‡∏Ç (Error Handling)
if location is not None:
    try:
        # ‡∏õ‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ô‡∏Å‡∏£‡∏ì‡∏µ‡πÇ‡∏Ñ‡∏£‡∏á‡∏™‡∏£‡πâ‡∏≤‡∏á Dictionary ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÅ‡∏õ‡∏•‡∏á
        curr_coords = location.get('coords', {})
        lat = curr_coords.get('latitude')
        lon = curr_coords.get('longitude')
        
        if lat and lon:
            st.success(f"‡∏û‡∏ö‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏à‡∏£‡∏¥‡∏á‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì‡πÅ‡∏•‡πâ‡∏ß!")
            
            col1, col2 = st.columns(2)
            col1.metric("‡∏•‡∏∞‡∏ï‡∏¥‡∏à‡∏π‡∏î", f"{lat:.4f}")
            col2.metric("‡∏•‡∏≠‡∏á‡∏à‡∏¥‡∏à‡∏π‡∏î", f"{lon:.4f}")
            
            st.write(f"‚è∞ ‡πÄ‡∏ß‡∏•‡∏≤‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô (‡πÑ‡∏ó‡∏¢): **{now_th.strftime('%H:%M:%S ‡∏ô.')}**")
            
            # ‡πÄ‡∏ï‡∏£‡∏µ‡∏¢‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÅ‡∏ú‡∏ô‡∏ó‡∏µ‡πà (Streamlit map ‡∏ä‡∏≠‡∏ö Format ‡πÅ‡∏ö‡∏ö DataFrame)
            map_data = pd.DataFrame({'lat': [lat], 'lon': [lon]})
            st.map(map_data)
        else:
            st.error("‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏£‡∏∞‡∏ö‡∏∏‡∏û‡∏¥‡∏Å‡∏±‡∏î‡∏ó‡∏µ‡πà‡πÅ‡∏ô‡πà‡∏ä‡∏±‡∏î‡πÑ‡∏î‡πâ ‡∏•‡∏≠‡∏á‡πÄ‡∏ä‡πá‡∏Å‡∏Å‡∏≤‡∏£‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ GPS ‡∏î‡∏π‡∏ô‡∏∞")
            
    except Exception as e:
        st.error(f"‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•: {e}")
else:
    # ‡∏ä‡πà‡∏ß‡∏á‡∏ó‡∏µ‡πà‡∏£‡∏≠‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• ‡∏´‡∏£‡∏∑‡∏≠ User ‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏Å‡∏î Allow
    st.info("‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏£‡∏≠‡∏Å‡∏≤‡∏£‡∏≠‡∏ô‡∏∏‡∏ç‡∏≤‡∏ï‡πÄ‡∏Ç‡πâ‡∏≤‡∏ñ‡∏∂‡∏á‡∏û‡∏¥‡∏Å‡∏±‡∏î... ‡∏´‡∏≤‡∏Å‡∏°‡∏µ Popup ‡πÄ‡∏î‡πâ‡∏á‡∏Ç‡∏∂‡πâ‡∏ô‡∏°‡∏≤ ‡πÉ‡∏´‡πâ‡∏Å‡∏î 'Allow' ‡∏´‡∏£‡∏∑‡∏≠ '‡∏≠‡∏ô‡∏∏‡∏ç‡∏≤‡∏ï' ‡∏ô‡∏∞‡∏Ñ‡∏£‡∏±‡∏ö")
    st.warning("‚ö†Ô∏è ‡∏´‡∏≤‡∏Å‡∏£‡∏≠‡∏ô‡∏≤‡∏ô‡πÅ‡∏•‡πâ‡∏ß‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏Ç‡∏∂‡πâ‡∏ô ‡∏≠‡∏≤‡∏à‡πÄ‡∏õ‡πá‡∏ô‡πÄ‡∏û‡∏£‡∏≤‡∏∞ Browser ‡∏ö‡∏•‡πá‡∏≠‡∏Å‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå ‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡∏£‡∏±‡∏ô‡∏ö‡∏ô HTTPS")

st.divider()
st.info("‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏´‡∏ï‡∏∏: ‡∏´‡∏≤‡∏Å‡πÉ‡∏ä‡πâ VPN ‡∏û‡∏¥‡∏Å‡∏±‡∏î‡∏à‡∏∞‡πÑ‡∏õ‡πÇ‡∏ú‡∏•‡πà‡∏ó‡∏µ‡πà Server ‡∏Ç‡∏≠‡∏á VPN ‡∏ô‡∏±‡πâ‡∏ô‡πÜ ‡πÅ‡∏ó‡∏ô")
