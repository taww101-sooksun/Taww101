            # ‡πÉ‡∏ä‡πâ Columns ‡∏à‡∏±‡∏î‡∏ß‡∏≤‡∏á‡πÉ‡∏´‡πâ‡∏î‡∏π‡∏á‡πà‡∏≤‡∏¢‡πÅ‡∏ö‡∏ö‡πÉ‡∏ô‡∏£‡∏π‡∏õ‡∏ó‡∏µ‡πà‡∏ô‡∏≤‡∏¢‡∏™‡πà‡∏á‡∏°‡∏≤
            col1, col2 = st.columns(2)
